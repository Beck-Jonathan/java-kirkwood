package en.codegym.task.pro.task04.task0404;

/* 
Filled rectangle
*/

public class Solution {
    public static void main(String[] args) {
        int count=0;
        int second_count=0;
        while (count<5) {
            while (second_count<10){
                System.out.print("Q");
                second_count++;
            }
            count+=1;
            System.out.print("\n");
            second_count=0;
        }
        }

    }
